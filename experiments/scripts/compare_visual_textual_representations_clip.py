#!/usr/bin/env python3
"""
Script to compare visual (image) and textual CLIP representations
using various similarity metrics.

Loads representations generated by extract_clip_representations.py from ImageNet
and computes approximate similarity scores using mini-batch sampling.
"""

import argparse
from pathlib import Path
import numpy as np
import pandas as pd

from src.baselines import run_approximate_baseline_measures
from src.tsi import BatchedTSI, ApproxTSI
from src.qsi import BatchedQSI, ApproxQSI
from src.data import RepresentationPair


CLIP_MODEL_SIZES = ["small", "medium", "large"]


def load_clip_representations(data_dir: Path, model_size: str, split: str) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Load CLIP image and text representations for a given model size from ImageNet.
    
    Args:
        data_dir: Directory containing the representation files
        model_size: One of 'small', 'medium', 'large'
        split: ImageNet split ('train' or 'val')
    
    Returns:
        Tuple of (image_representations, text_representations, labels)
    """
    image_path = data_dir / f"imagenet_{split}_image_representations_{model_size}.npy"
    text_path = data_dir / f"imagenet_{split}_text_representations_{model_size}.npy"
    labels_path = data_dir / f"imagenet_{split}_labels_{model_size}.npy"
    
    if not image_path.exists():
        raise FileNotFoundError(f"Image representations not found: {image_path}")
    if not text_path.exists():
        raise FileNotFoundError(f"Text representations not found: {text_path}")
    
    image_repr = np.load(image_path)
    text_repr = np.load(text_path)
    labels = np.load(labels_path) if labels_path.exists() else None
    
    return image_repr, text_repr, labels


def compute_approx_scores(
    X: np.ndarray, 
    Y: np.ndarray, 
    run_seed: int,
    batch_size: int, 
    no_batches: int,
    n_threads: int
) -> dict:
    """
    Compute approximate similarity scores between two representation matrices.
    
    Args:
        X: First representation matrix (image representations)
        Y: Second representation matrix (caption representations)
        run_seed: Random seed for reproducibility
        batch_size: Mini-batch size for approximate methods
        no_batches: Number of batches to sample
        n_threads: Number of threads for parallel computation
    
    Returns:
        Dictionary of similarity scores
    """
    print(f"  [Approx seed={run_seed}] Computing approximate baselines (batch_size={batch_size}, no_batches={no_batches})...")
    
    # Approximate baselines (mini-batch sampling)
    approx_baselines = run_approximate_baseline_measures(
        X, Y, batch_size=batch_size, no_batches=no_batches, seed=run_seed
    )

    # Approximate TSI/QSI (batch-based efficient variants)
    d = lambda a, b: np.linalg.norm(a - b)
    representations = RepresentationPair(X=X, Y=Y, d_x=d, d_y=d)

    print(f"  [Approx seed={run_seed}] Computing BatchedTSI...")
    approx_tsi_efficient = BatchedTSI(
        euclidean=True, 
        n_threads=n_threads,
        batch_size=batch_size, 
        no_batches=no_batches, 
        seed=run_seed
    )(representations)
    print(f"  [Approx seed={run_seed}] BatchedTSI done: {approx_tsi_efficient:.6f}")

    print(f"  [Approx seed={run_seed}] Computing BatchedQSI...")
    approx_qsi_efficient = BatchedQSI(
        euclidean=True, 
        n_threads=n_threads,
        batch_size=batch_size, 
        no_batches=no_batches, 
        seed=run_seed
    )(representations)
    print(f"  [Approx seed={run_seed}] BatchedQSI done: {approx_qsi_efficient:.6f}")

    # Also compute sampling-based ApproxTSI/QSI with equivalent number of samples
    approx_n_samples = (batch_size ** 2) * no_batches
    print(f"  [Approx seed={run_seed}] Computing ApproxTSI/QSI with n_samples={approx_n_samples}...")
    
    approx_tsi_sampling = ApproxTSI(
        n_samples=approx_n_samples, 
        n_threads=n_threads, 
        seed=run_seed
    )(representations)
    print(f"  [Approx seed={run_seed}] ApproxTSI(n_samples={approx_n_samples}) done: {approx_tsi_sampling:.6f}")
    
    approx_qsi_sampling = ApproxQSI(
        n_samples=approx_n_samples, 
        n_threads=n_threads, 
        seed=run_seed
    )(representations)
    print(f"  [Approx seed={run_seed}] ApproxQSI(n_samples={approx_n_samples}) done: {approx_qsi_sampling:.6f}")

    # Compute C-TSI/C-QSI with negative cosine similarity as distance
    # d(a, b) = -cosine_similarity(a, b) = -dot(a, b) / (||a|| * ||b||)
    # Higher cosine similarity means lower distance
    d_cossim = lambda a, b: -np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b))
    representations_cossim = RepresentationPair(X=X, Y=Y, d_x=d_cossim, d_y=d_cossim)
    
    print(f"  [Approx seed={run_seed}] Computing ApproxTSI/QSI with negative cosine similarity distance...")
    
    approx_tsi_cossim = ApproxTSI(
        n_samples=approx_n_samples, 
        n_threads=n_threads, 
        seed=run_seed
    )(representations_cossim)
    print(f"  [Approx seed={run_seed}] ApproxTSI-CosSim done: {approx_tsi_cossim:.6f}")
    
    approx_qsi_cossim = ApproxQSI(
        n_samples=approx_n_samples, 
        n_threads=n_threads, 
        seed=run_seed
    )(representations_cossim)
    print(f"  [Approx seed={run_seed}] ApproxQSI-CosSim done: {approx_qsi_cossim:.6f}")

    result = {
        'B-TSI': approx_tsi_efficient,
        'B-QSI': approx_qsi_efficient,
        'C-TSI': approx_tsi_sampling,
        'C-QSI': approx_qsi_sampling,
        'C-TSI-CosSim': approx_tsi_cossim,
        'C-QSI-CosSim': approx_qsi_cossim,
    }
    result.update(approx_baselines)
    return result


def main():
    parser = argparse.ArgumentParser(
        description='Compare visual and textual CLIP representations using similarity metrics'
    )
    parser.add_argument(
        '--data-dir',
        type=str,
        default='experiments/results/clip_representations',
        help='Directory containing CLIP representation files'
    )
    parser.add_argument(
        '--output-dir',
        type=str,
        default='experiments/results/clip_visual_textual_comparison',
        help='Directory to save comparison results'
    )
    parser.add_argument(
        '--split',
        type=str,
        default='val',
        choices=['train', 'val'],
        help='ImageNet split to use (train or val)'
    )
    parser.add_argument(
        '--models',
        nargs='+',
        choices=['small', 'medium', 'large', 'all'],
        default=['all'],
        help='Which CLIP models to compare (small, medium, large, or all)'
    )
    parser.add_argument(
        '--seed',
        type=int,
        default=0,
        help='Base random seed'
    )
    parser.add_argument(
        '--approx-runs',
        type=int,
        default=1,
        help='Number of approximation runs with different seeds'
    )
    parser.add_argument(
        '--batch-size',
        type=int,
        default=1000,
        help='Mini-batch size for approximate methods'
    )
    parser.add_argument(
        '--no-batches',
        type=int,
        default=10,
        help='Number of batches for approximate methods'
    )
    parser.add_argument(
        '--n-threads',
        type=int,
        default=8,
        help='Number of threads for parallel approximate methods'
    )
    
    args = parser.parse_args()
    
    # Determine which models to process
    if 'all' in args.models:
        model_sizes = CLIP_MODEL_SIZES
    else:
        model_sizes = args.models
    
    data_dir = Path(args.data_dir)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    
    print("=" * 70)
    print("CLIP Visual vs Textual Representation Comparison (ImageNet)")
    print("=" * 70)
    print(f"Data directory: {data_dir}")
    print(f"Output directory: {output_dir}")
    print(f"Split: {args.split}")
    print(f"Models: {model_sizes}")
    print(f"Seed: {args.seed}, Approx runs: {args.approx_runs}")
    print(f"Batch size: {args.batch_size}, No. batches: {args.no_batches}")
    print(f"N threads: {args.n_threads}")
    print()
    
    all_results = []
    
    for model_size in model_sizes:
        print("-" * 70)
        print(f"Processing model: {model_size}")
        print("-" * 70)
        
        try:
            # Load representations
            print(f"Loading {model_size} representations...")
            image_repr, text_repr, labels = load_clip_representations(data_dir, model_size, args.split)
            
            n_samples = image_repr.shape[0]
            image_dim = image_repr.shape[1]
            text_dim = text_repr.shape[1]
            
            print(f"  Image representations: {image_repr.shape}")
            print(f"  Text representations: {text_repr.shape}")
            if labels is not None:
                print(f"  Labels: {labels.shape}")
            
            # Run approximation runs
            for run_idx in range(args.approx_runs):
                run_seed = args.seed + 1000 + run_idx
                print(f"\n  Run {run_idx + 1}/{args.approx_runs} (seed={run_seed})")
                
                scores = compute_approx_scores(
                    X=image_repr,
                    Y=text_repr,
                    run_seed=run_seed,
                    batch_size=args.batch_size,
                    no_batches=args.no_batches,
                    n_threads=args.n_threads
                )
                
                # Build result row
                row = {
                    'model_size': model_size,
                    'split': args.split,
                    'run': run_idx + 1,
                    'seed': run_seed,
                    'n_samples': n_samples,
                    'image_dim': image_dim,
                    'text_dim': text_dim,
                    'batch_size': args.batch_size,
                    'no_batches': args.no_batches,
                }
                
                for k, v in scores.items():
                    row[k] = np.nan if v is None else v
                
                all_results.append(row)
                
            print(f"\n  Model {model_size} completed.")
            
        except FileNotFoundError as e:
            print(f"  Skipping {model_size}: {e}")
            continue
    
    if not all_results:
        print("\nNo results to save. Make sure representation files exist in the data directory.")
        return
    
    # Create DataFrame and save
    results_df = pd.DataFrame(all_results)
    
    # Save results
    output_path = output_dir / f"imagenet_{args.split}_visual_textual_comparison_batch{args.batch_size}_nb{args.no_batches}_runs{args.approx_runs}_seed{args.seed}.csv"
    results_df.to_csv(output_path, index=False)
    
    print()
    print("=" * 70)
    print("Results Summary")
    print("=" * 70)
    
    # Print summary statistics per model
    score_cols = [c for c in results_df.columns if c not in ['model_size', 'split', 'run', 'seed', 'n_samples', 'image_dim', 'text_dim', 'batch_size', 'no_batches']]
    
    for model_size in model_sizes:
        model_df = results_df[results_df['model_size'] == model_size]
        if len(model_df) == 0:
            continue
        
        print(f"\n{model_size.upper()} Model:")
        for col in score_cols:
            values = model_df[col].dropna()
            if len(values) > 0:
                if len(values) > 1:
                    print(f"  {col}: {values.mean():.4f} ± {values.std():.4f}")
                else:
                    print(f"  {col}: {values.iloc[0]:.4f}")
    
    print()
    print(f"Results saved to: {output_path}")
    print("=" * 70)


if __name__ == "__main__":
    main()
